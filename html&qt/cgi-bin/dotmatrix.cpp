#define CHAR_COUNT (sizeof(GB_16) / sizeof(GB_16[0]))

int main()
{
    int mem_fd = open("/dev/mem", O_RDWR);
    if (mem_fd < 0) {
        printf("Content-Type: text/html; charset=utf-8\r\n\r\n");
        printf("open /dev/mem failed\n");
        return 1;
    }

    volatile unsigned short* cpld = (volatile unsigned short*)mmap(
        NULL, 0x400, PROT_READ | PROT_WRITE,
        MAP_SHARED, mem_fd, 0x8000000);

    if (cpld == MAP_FAILED) {
        printf("Content-Type: text/html; charset=utf-8\r\n\r\n");
        printf("mmap failed\n");
        close(mem_fd);
        return 1;
    }

    pid_t pid = fork();
    if (pid < 0) {
        printf("Content-Type: text/html; charset=utf-8\r\n\r\n");
        printf("fork failed\n");
        return 1;
    }

    if (pid > 0) {
        printf("Content-Type: text/html; charset=utf-8\r\n\r\n");
        printf("<html><body>");
        printf("<h3>点阵已启动，正在滚动显示：多模态视觉分析系统</h3>");
        printf("<p>后台进程 PID: %d</p>", pid);
        printf("<p>停止显示请执行: kill %d</p>", pid);
        printf("</body></html>");
        return 0;
    }

    setsid();

    while (1) {
        for (int z = 0; z < CHAR_COUNT; z++) {
            for (int j = 0; j < 16; j++) {
                for (int i = 0; i < 16; i++) {
                    unsigned short val;
                    if (i < 16 - j) {
                        val = (GB_16[z].Msk[(i + j) * 2] << 8)
                            | GB_16[z].Msk[(i + j) * 2 + 1];
                    } else {
                        int nextZ = (z + 1) % CHAR_COUNT;
                        val = (GB_16[nextZ].Msk[(i - (16 - j)) * 2] << 8)
                            | GB_16[nextZ].Msk[(i - (16 - j)) * 2 + 1];
                    }
                    cpld[2 * (0xc0 + i)] = val;
                }
                usleep(200000);
            }
        }
    }

    munmap((void*)cpld, 0x400);
    close(mem_fd);
    return 0;
}
